// Helper function to get icon HTML
export function getIcon(name, className = '', size = 20) {
    const icon = PremiumIcons[name];
    if (!icon) {
        console.warn(`Icon "${name}" not found`);
        return '';
    }

    return icon
        .replace('<svg', `<svg width="${size}" height="${size}" class="${className}"`)
        .trim();
}