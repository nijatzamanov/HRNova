export function getMenuItemByKey(key) {
    return menuConfig.find(item => item.key === key);
}

export function getMenuItemByHref(href) {
    return menuConfig.find(item => item.href === href);
}

export function filterMenuByPermissions(permissions = []) {
    if (!permissions || permissions.length === 0) {
        return menuConfig;
    }

    return menuConfig.filter(item => {
        if (!item.requiredPermission) return true;
        return permissions.includes(item.requiredPermission) || permissions.includes('*');
    });
}
